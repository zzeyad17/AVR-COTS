	#define CLCD_u8_D5_PIN		DIO_u8_PIN5
	#define CLCD_u8_D6_PIN		DIO_u8_PIN6
	#define CLCD_u8_D7_PIN		DIO_u8_PIN7
#endif
/********************************************************
*		         SELECT CLCD CTRL PORT					*
********************************************************/
#define CLCD_u8_CTRL_PORT	DIO_u8_PORTB

#define CLCD_u8_RS_PIN		DIO_u8_PIN2
#define CLCD_u8_RW_PIN      DIO_u8_PIN1
#define CLCD_u8_E_PIN		DIO_u8_PIN0
/********************************************************
*		         SELECT CLCD CTRL PORT					*
********************************************************/
#endif /*CLCD_CONFIG_H_*/
